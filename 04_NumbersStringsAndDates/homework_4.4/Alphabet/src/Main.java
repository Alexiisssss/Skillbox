public class Main {

  public static void main(String[] args) {

    // englishAlphabet = "abcdefghijklmnopqrstuvwxyz"
    for (char i = 'a'; i <= 'z'; i++) {
      int c = i;
      System.out.println(i + " :" + c + ".");
    }
    // russianAlphabet = "абвгдежзийклмнопрстуфцчшщъыьэюя"
    for (char j = 'а'; j <= 'я'; j++) {
      int s = j;
      System.out.println(j + " :" + s + ".");
    }
  }
}


